package com.evergent.corejava.arrays;

public class DDArray6 {

			public static void main(String[] args) {
				int arr[][] = new int[5][5];
				int count=1;
				for(int i=1;i<5;i++)
				{
					for(int j=1;j<5;j++)
					{
						System.out.print(arr[i][j]=count);
						count++;
					}
					System.out.println();
				}

			}

		


	}

